package com.kimjio.coral.data.splat;

public class Gear {
    private String id;
    private String name;
    private String kind;
    private String image;
    private String thumbnail;
    private int rarity;
    private Brand brand;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getKind() {
        return kind;
    }

    public String getImage() {
        return image;
    }

    public String getThumbnail() {
        return thumbnail;
    }

    public int getRarity() {
        return rarity;
    }

    public Brand getBrand() {
        return brand;
    }
}
